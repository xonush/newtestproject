package lesson6;

public class MethodOverloading {
    public static int sum(int a,int b){
        return a+b;
    }

    public static int sumeThreeNumbers(int a,int b,int c){
        return a+b+c;
    }

    public static double sumTwoDoubles(double a,double b){
        return a+b;
    }
    public static double sumThreeDoubles(double a,double b,double c){
        return a+b+c;
    }
}
